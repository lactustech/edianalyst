/**
 * Denial-code reference data for /edi/835/denial-codes. One entry per code
 * drives one statically-generated page (see
 * app/edi/[code]/denial-codes/[carc]/page.tsx), because denial analysts search
 * individual codes — "CO-45", "PR-1", "N130" — one at a time.
 *
 * Two code systems appear on an 835 remittance:
 *   - CARC (Claim Adjustment Reason Codes): why an amount wasn't paid. Numeric,
 *     always paired with a group code (CO / PR / OA / PI / CR) in the CAS segment.
 *   - RARC (Remittance Advice Remark Codes): extra context. Start with M, N, or MA.
 *
 * All prose here is our own plain-English wording — nothing is copied from the
 * official X12/WPC code lists. The `short` field paraphrases the standard
 * description; `plain`, `why`, and `fix` are explanatory. This is a curated set
 * of the codes analysts hit most; the official lists are maintained by X12 and
 * new entries can be appended here as they're needed.
 */
export type CodeSystem = "CARC" | "RARC";

/** X12 claim adjustment group codes — who the adjustment belongs to. */
export type GroupCode = "CO" | "PR" | "OA" | "PI" | "CR";

export interface DenialCode {
  code: string; // "45", "97", "N130"
  system: CodeSystem;
  /** Typical group code for a CARC (the prefix in "CO-45"). RARCs have none. */
  group?: GroupCode;
  /** Other group codes this CARC commonly appears under. */
  alsoGroups?: GroupCode[];
  /** One-line paraphrase of the standard description. */
  short: string;
  /** What it actually means, in plain English. */
  plain: string;
  /** Why a payer sends it. */
  why: string;
  /** What a billing team typically does about it. */
  fix: string;
  category: CategoryKey;
}

export type CategoryKey =
  | "patient"
  | "contractual"
  | "coverage"
  | "auth"
  | "coding"
  | "duplicate"
  | "timely"
  | "cob"
  | "info";

