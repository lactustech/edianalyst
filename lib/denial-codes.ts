/**
 * Slug used in the URL for a code — group-prefixed so it matches how analysts
 * search. CARC → "co-45", "pr-1"; RARC (no group) → "n130", "m15".
 */
export function codeSlug(c: DenialCode): string {
  return (c.group ? `${c.group}-${c.code}` : c.code).toLowerCase();
}

/** Display label including the typical group code, e.g. "CO-45" or "N130". */
export function codeLabel(c: DenialCode): string {
  return c.group ? `${c.group}-${c.code}` : c.code;
}

export function getDenialCode(slug: string): DenialCode | undefined {
  return DENIAL_CODES.find((c) => codeSlug(c) === slug.toLowerCase());
}

/** A few related codes — same category first, then fill from the same system. */
export function relatedCodes(c: DenialCode, n = 6): DenialCode[] {
  const others = DENIAL_CODES.filter((x) => x.code !== c.code);
  const sameCat = others.filter((x) => x.category === c.category);
  const rest = others.filter((x) => x.category !== c.category && x.system === c.system);
  return [...sameCat, ...rest].slice(0, n);
}
